# write a python program to calculate the square of two numbers entered by the user 
a = int(input("enter the first number: "))
b = int(input("enter the second number:"))
squareA = a*a # we can also write square in the form of a**2
squareB = b*b # we can also write square in the form of b**2
print("the square of first number is:",squareA)
print("the square of second number is:",squareB)